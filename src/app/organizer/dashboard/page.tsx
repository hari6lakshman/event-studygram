"use client"
import { useState } from "react";
import { Button } from "@/components/ui/button";
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card";
import { CreateEventForm, NewEventType } from "@/components/create-event-form";
import { events as initialEvents, Event } from "@/lib/events";
import { PlusCircle } from "lucide-react";
import { Badge } from "@/components/ui/badge";

// For now, we'll assume organizer with ID "1" is logged in.
const ORGANIZER_ID = "1";

export default function OrganizerDashboardPage() {
  const [isCreatingEvent, setIsCreatingEvent] = useState(false);
  // We use a state to manage the events list, initialized with the static data.
  // This will allow us to add new events to it.
  const [events, setEvents] = useState<Event[]>(initialEvents);

  // We derive the organizer's events from the main events state.
  const organizerEvents = events.filter(event => event.organizerId === ORGANIZER_ID);

  const handleEventCreated = (newEventData: NewEventType) => {
    const newEvent: Event = {
      // In a real app, the ID would be generated by the database
      id: (initialEvents.length + 1 + Math.random()).toString(), 
      ...newEventData,
      tags: newEventData.tags.split(',').map(tag => tag.trim()),
      // A default image for newly created events
      organizerId: ORGANIZER_ID,
      longDescription: newEventData.longDescription,
    };
    // By updating the state here, any component that uses `events` will re-render.
    // However, this state is not persisted across page reloads.
    // For a real app, we would write this to a database.
    const updatedEvents = [newEvent, ...events];
    setEvents(updatedEvents);
    initialEvents.unshift(newEvent); // This is a temporary solution for this prototype
    
    setIsCreatingEvent(false);
  }

  return (
    <div className="container mx-auto px-4 py-12">
      <div className="flex justify-between items-center mb-8">
        <h1 className="text-4xl font-bold text-primary">Organizer Dashboard</h1>
        <Button onClick={() => setIsCreatingEvent(!isCreatingEvent)}>
          <PlusCircle className="mr-2" />
          {isCreatingEvent ? "Cancel" : "Create New Event"}
        </Button>
      </div>

      {isCreatingEvent ? (
        <CreateEventForm onFinished={() => setIsCreatingEvent(false)} onEventCreated={handleEventCreated} />
      ) : (
        <Card className="border-4 border-primary">
          <CardHeader>
            <CardTitle>Your Events</CardTitle>
            <CardDescription>Here are the events you've created.</CardDescription>
          </CardHeader>
          <CardContent className="space-y-6">
            {organizerEvents.length > 0 ? (
              organizerEvents.map(event => (
                <div key={event.id} className="flex items-center gap-4 p-4 border-4 border-primary rounded-lg">
                  <div className="flex-1">
                    <h3 className="text-lg font-semibold">{event.title}</h3>
                    <p className="text-sm text-muted-foreground">{event.date} &bull; {event.location}</p>
                     <div className="flex flex-wrap gap-2 mt-2">
                        {event.tags.map(tag => <Badge key={tag} variant="secondary">{tag}</Badge>)}
                    </div>
                  </div>
                  <Button variant="outline" size="sm">Edit</Button>
                </div>
              ))
            ) : (
              <p className="text-center text-muted-foreground py-8">You haven't created any events yet.</p>
            )}
          </CardContent>
        </Card>
      )}
    </div>
  );
}
